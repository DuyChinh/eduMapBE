if (typeof globalThis.ImageData === 'undefined') {
  globalThis.ImageData = class ImageData {
    constructor(sw, sh, settings) {
      if (typeof sw === 'object') {
        this.data = sw;
        this.width = sh;
        this.height = settings;
      } else {
        this.width = sw;
        this.height = sh;
        this.data = new Uint8ClampedArray(sw * sh * 4);
      }
      this.colorSpace = 'srgb';
    }
  };
}

